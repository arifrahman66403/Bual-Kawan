        <div class="profile-content">
            <p><strong>Pemerintah Kabupaten Siak</strong> adalah pusat pemerintahan dan administrasi untuk wilayah Kabupaten Siak, Provinsi Riau. Berdiri sejak pemekaran wilayah, Pemkab Siak berkomitmen untuk mewujudkan visi daerah yang fokus pada pembangunan berkelanjutan, pelayanan publik yang prima, dan pelestarian nilai-nilai budaya Melayu.</p>
            
            <img src="https://picsum.photos/id/55/1000/500" alt="Foto Gedung Kantor Bupati Siak">
            
            <h2>Sejarah Singkat Pembentukan</h2>
            <p>Kabupaten Siak dibentuk berdasarkan Undang-Undang Nomor 53 Tahun 1999. Secara historis, wilayah ini merupakan pusat Kerajaan Siak Sri Indrapura, salah satu kerajaan Melayu terbesar di Riau. Semangat sejarah ini terus diabadikan dalam tata kelola pemerintahan dan pembangunan daerah.</p>
            
            <p>Sejak pembentukannya, Pemkab Siak telah melalui beberapa periode kepemimpinan dan fokus pada pengembangan infrastruktur dasar, ekonomi kerakyatan, dan pendidikan. Perkembangan penting meliputi:</p>
            
            <ul>
                <li><strong>1999:</strong> Penetapan sebagai Kabupaten.</li>
                <li><strong>2005-2010:</strong> Fokus pada pembangunan jalan dan infrastruktur perkebunan.</li>
                <li><strong>2015-Sekarang:</strong> Era digitalisasi dan inisiatif Smart City.</li>
            </ul>

            <h2>Struktur Organisasi</h2>
            <p>Pemerintahan Kabupaten Siak dipimpin oleh Bupati dan Wakil Bupati yang dibantu oleh Sekretaris Daerah (Sekda) dan jajaran Organisasi Perangkat Daerah (OPD). Berikut adalah contoh struktur OPD utama:</p>
            
            <table>
                <thead>
                    <tr>
